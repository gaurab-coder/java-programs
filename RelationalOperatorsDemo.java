class RelationalOperatorsDemo
{
    static public void main(String af[])
    {
        
        int a=5, b=11;
        System.err.println("a is"+a+" and b is"+b);
        System.err.println(a==b);
        System.err.println(a!=b);
        System.err.println(a>b);
        System.err.println(a<b);
        System.err.println(a>=b);
        System.err.println(a<=b);
    }
}